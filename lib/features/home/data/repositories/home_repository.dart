import '../models/style_image.dart';

class HomeRepository {
  Future<List<StyleImage>> getCuratedStyles({
    int page = 0,
    String? tag,
  }) async {
    await Future.delayed(const Duration(milliseconds: 500));
    
    final allImages = _getAllLocalImages();
    
    if (tag != null && tag.isNotEmpty && tag != 'Recommended') {
      return allImages.where((img) => img.tags.contains(tag)).toList();
    }
    
    return allImages;
  }

  Future<List<StyleImage>> searchStyles(String query) async {
    await Future.delayed(const Duration(milliseconds: 300));
    return _getAllLocalImages()
        .where((img) => 
            img.tags.any((tag) => tag.toLowerCase().contains(query.toLowerCase())) ||
            (img.description?.toLowerCase().contains(query.toLowerCase()) ?? false))
        .toList();
  }

  Future<List<StyleImage>> getSimilarStyles(String imageId) async {
    await Future.delayed(const Duration(milliseconds: 400));
    final allImages = _getAllLocalImages();
    final currentImage = allImages.firstWhere((img) => img.id == imageId);
    
    return allImages
        .where((img) => 
            img.id != imageId && 
            img.tags.any((tag) => currentImage.tags.contains(tag)))
        .take(6)
        .toList();
  }

