/// 捕获异步错误的辅助函数
void runZonedGuarded(
  void Function() body,
  void Function(Object, StackTrace) onError,
) {
  // 在Flutter中直接运行，但保留错误处理逻辑
  try {
    body();
  } catch (e, stack) {
    onError(e, stack);
  }
}

