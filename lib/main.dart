/// 粒子模型
class ParticleModel {
  late double x;
  late double y;
  late double size;
  late Color color;
  late double speed;

  // 随机生成粒子
  ParticleModel.random() {
    final random = DateTime.now().millisecondsSinceEpoch % 10000 / 10000;
    x = random * 400;
    y = random * 800;
    size = 1 + random * 4;
    speed = 1 + random * 3;
    color = ColorUtils.withOpacity(AppTheme.champagne, 0.1 + random * 0.4);
  }
}

/// 粒子效果绘制
class ParticlePainter extends CustomPainter {
  final List<ParticleModel> particles;
  final Animation<double> animation;

  ParticlePainter({required this.particles, required this.animation})
      : super(repaint: animation);

  @override
  void paint(Canvas canvas, Size size) {
    for (var particle in particles) {
      final paint = Paint()..color = particle.color;

      // 计算粒子位置，使其随时间移动
      double offsetY = (animation.value * particle.speed * 100) % size.height;
      double y = (particle.y - offsetY) % size.height;

      // 绘制粒子
      canvas.drawCircle(
        Offset(particle.x % size.width, y),
        particle.size,
        paint,
      );
    }
  }

  @override
  bool shouldRepaint(covariant CustomPainter oldDelegate) => true;
}
